                                                                    ประเมินสารอาหารจากอาหารที่กินในแต่ละวันแบบ “ง่าย ๆ” “คร่าว ๆ” “เบสิค ๆ” <br>
                                                                    การทำให้รูปร่างดี สุขภาพแข็งแรง กล้ามโตหรือเฟิร์ม เป็นสิ่งที่อาศัยสองสิ่งควบคู่กันไปคือ การออกกำลังกายที่เหมาะสมและ การรับประทานอาหารที่พอเหมาะ ไม่มากหรือน้อยเกินไป แต่หลาย ๆ
                                                                    คนที่กำลังดูแลรูปร่างมักไม่สะดวกในการควบคุมอาหาร โดยเฉพาะคนที่มีการใช้ชีวิตแบบคนในเมืองที่ต้องเร่งรีบและแข่งขันตลอดเวลา ทำให้ต้องชีวิตในลักษณะ “หาอะไรกินได้ก็กิน”​ และมักลงท้ายด้วยการที่ 
                                                                    “มีน.น.ไขมันเกิน” เพราะอาหารที่วางขายทั่วไป มักอยู่ในลักษณะของ “คาร์บเยอะ (จากข้าวปริมาณมาก) โปรตีนน้อย (ต้นทุนเนื้อมันแพง) ไขมันเยอะ​ (ใส่น้อยกลัวผัดแล้วติดกระทะ) และยังไม่ค่อยได้รับประทานผักและผลไม้อีก (ราคาแพงกว่าขนม)” อย่างไรก็ตาม ทางออกเพื่อสุขภาพย่อมมีเสมอและการใช้ชีวิตให้สมดุลเป็นสิ่งเหมาะสมที่สุด

                                                                    <br> <br>

                                                                    การประเมินสารอาหารแบบง่าย ๆ <br>
                                                                    หัวใจหลักของการกะสารอาหารคือ แยกหมวด ข้าวแป้งเส้น / เนื้อสัตว์ / ผัก / ผลไม้ และกะปริมาณให้เข้าตามหน่วยวัดที่เหมาะสม ได้แก่ <br>
                                                                    <br>
                                                                    – ข้าว เส้น ใช้หน่วยเป็นทัพพี ขนมปัง เป็นแผ่น <br>
                                                                    – เนื้อสัตว์ เต้าหู้ เต้าหู้หลอด ใช้หน่วยเป็นช้อนโต๊ะ ไข่ นับเป็นฟอง <br>
                                                                    – ผลไม้ ให้นับแยกเป็นชนิดใด และนับเป็นลูก กะง่าย ๆ ว่าผลไม้ไม่หวานจัด ถ้าตัดแบ่งหรือกินขนาดเท่ากำปั้นจะให้พลังงาน 60 kcal <br>
                                                                    – ผัก นับเป็นช้อนโต๊ะหรือทัพพีก็ได้ <br> <br>
                                                                    

                                                                    ต้องแยกอาหารต่าง ๆ ในจาน ในกล่อง ในเมนูหรือในชุดออกมาก่อน มองให้เป็นอาหาร 5 หมู่ <br>
                                                                    หมู่ข้าว แป้ง เส้น ขนมปัง ในแต่ละข้อ ให้พลังงาน 80 kcal ทั้งหมด แต่แตกต่างกันออกไปในน้ำหนักอาหารและสารอาหารภายใน <br>
                                                                    ข้าวทุกชนิด (ข้าวเจ้า ข้าวหอมมะลิ ข้าวกล้อง ข้าวมันปู ข้าวไรซ์เบอร์รี่) 1 ทัพพี ไม่อัดหรือกดแน่น จะมีน้ำหนัก 50 กรัม ให้คาร์โบไฮเดรต 18 กรัม และโปรตีน 1.2 กรัม
                                                                    ข้าวเหนียว ครึ่งทัพพี (หรือครึ่งถุงข้าวเหนียวขนาดฝ่ามือ) จะมีน้ำหนัก 30 กรัม ให้คาร์โบไฮเดรต 18 กรัม และโปรตีน 1.2 กรัม
                                                                    เส้นทุกชนิด (เส้นเล็ก เส้นใหญ่ เส้นหมี่ วุ้นเส้น ยกเว้นบะหมี่) 1 ทัพพีสูงกว่าข้าวเล็กน้อย มีน้ำหนักประมาณ 90-100 กรัม ให้คาร์โบไฮเดรต 18 กรัม และโปรตีนประมาณ 1.5 กรัม ส่วนบะหมี่เหลืองและบะหมี่หยก ประมาณ 1 ทัพพีข้าว หนักประมาณ 80 กรัม จะให้คาร์โบไฮเดรต 18 กรัมและโปรตีน 2 กรัม
                                                                    ขนมปัง (ขนมปังแผ่น ขนมปังโฮลวีต ขนมปังโฮลเกรน) ขนาด 1 แผ่นฟาร์มเฮาส์ มีน้ำหนักประมาณ 25 กรัม ให้คาร์โบไฮเดรต 15 กรัม และโปรตีน 2 กรัม ถ้าเป็นขนมปังกะโหลก ขนมปังปอนด์หรือใหญ่กว่าขึ้น ให้เทียบจากขนาดขนมปังฟาร์มเฮาส์ว่า คิดเป็นกี่แผ่นของขนมปังแผ่นปกติ
                                                                    <br> <br>

                                                                    หมู่เนื้อสัตว์ จะคิดจากแหล่งอาหารที่ปรุงสุกแล้ว ในแต่ละข้อจะให้โปรตีนเท่ากันที่ 7 กรัม แต่ให้พลังงานแตกต่างกัน เพราะมีปริมาณไขมันแตกต่างกัน
                                                                    เนื้อปลาไม่มีหนัง เนื้อไก่ไม่ติดหนัง กุ้ง ปู หอย ปลาหมึก 2 ช้อนโต๊ะ จะให้พลังงาน 40 kcal มาพร้อมกับโปรตีน 7 กรัมและไขมัน 1 กรัม
                                                                    เนื้อหมูไม่ติดมันหรือหนัง เนื้อเป็ดไม่มีหนัง 2 ช้อนโต๊ะ เต้าหู้แข็งครึ่งแผ่น (ตัดเป็นรูปสามเหลี่ยม) ไข่ไก่ 1 ฟอง ให้พลังงาน 75 kcal และไขมัน 5 กรัม
                                                                    เนื้อปลามีหนัง เนื้อไก่ติดหนัง เนื้อหมูติดมัน เนื้อวัวติดมัน เนื้อสัตว์แปรรูป (โบโลน่า ไส้กรอก แฮม) ให้ปริมาณ 2 ช้อนโต๊ะ ให้พลังงาน 100 kcal และไขมัน 8 กรัม
                                                                    <br> <br>

                                                                   ไขมันและน้ำมัน
                                                                     ไขมันล้วน เช่น เนย เนยเทียม น้ำมันต่างๆ 1 ช้อนโต๊ะ (ปริมาณ 15 กรัม) ให้พลังงาน 135 kcal ซึ่งเป็นไขมันทั้งหมด 15 กรัม
                                                                     ไขมันผสม เช่น กะทิ มายองเนส ครีม 1 ช้อนโต๊ะ ให้พลังงาน 45 kcal ให้ไขมัน 5 กรัม
                                                                     <br> <br>

                                                                   ผลไม้ ให้พลังงาน 60 kcal และมีคาร์โบไฮเดรต 15 กรัมและโปรตีน 1 กรัม เท่ากันทั้งหมด แต่ไม่นิยมคำนวณปริมาณโปรตีนจากผลไม้รวมเข้าไปด้วย
                                                                    ผลไม้สด หวานน้อย ทุกชนิด ขนาดรับประทานประมาณ 1 กำหมัด เช่น แอปเปิ้ลแจ๊ส 1 ลูก ฝรั่งครึ่งลูก ชมพู่ 2 ลูก ส้มโอ 1 กลีบ
                                                                    ผลไม้สด รสหวาน เช่น มังคุด เงาะ ละมุด ให้นับเป็น 1 กำมือล้วงลงถุง จะได้ประมาณ 3-5 ลูก
                                                                     ผลไม้อื่นๆ ที่อยู่นอกเหนือนี้ ให้ยึดหลัก 1 กำปั้นเราเอง ให้พลังงาน 60 kcal และ คาร์บ 15 กรัม
                                                                     <br> <br>

                                                                   ผัก ที่รับประทานก้าน ใบทุกชนิด ไม่นับพลังงาน
                                                                   <br>

                                                                   หลังจากแยกหมวดหมู่อาหารในจาน มองออกมาแล้ว ให้ทำการ กะประมาณว่า ต้องการรับประทานเพื่อเอาสารอาหารใด เท่าไหร่บ้าง เช่น
